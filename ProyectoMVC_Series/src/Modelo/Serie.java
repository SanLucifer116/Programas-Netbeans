package Modelo;

/**
 *
 * @author HP
 */
public class Serie {
    
private int min;
private String nombre;
private int año;

    public Serie() {
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getAño() {
        return año;
    }

    public void setAño(int año) {
        this.año = año;
    }






}
